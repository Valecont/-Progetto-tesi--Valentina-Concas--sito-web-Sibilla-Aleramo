function mostraSezione(nomeSezione, box) {
  var dati = sezioni[nomeSezione];
  box.querySelector("h4").innerHTML = dati.titolo;
  box.querySelector("img").src = dati.immagine;

  var paragrafi = box.querySelectorAll(".paragrafo");
  for (var i = 0; i < paragrafi.length; i++) {
    paragrafi[i].innerHTML = dati.testi[i];
  }

  box.style.display = "block";
}


function chiudiBox(event) {
    var contenitore = event.target.parentNode;
    contenitore.style.display ="none"
}

